function search(city) {
  let apiKey = "1b92415f0f2a81ca6f05973c2626e33c";
  let apiUrl = `https://api.openweathermap.org/data/2.5/weather?q=${city}&appid=${apiKey}&units=metric`;
  axios.get(apiUrl).then(showTemperature);
}

